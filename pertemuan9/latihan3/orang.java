package pertemuan9.latihan3;

public class orang {
    protected String nama;
    protected String alamat;
    
    public orang(String nama, String alamat) {
        this.nama = "emmir";
        this.alamat = "bintan";
    }
    public String getNama() {
        return nama;
    }

    public String getAlamat() {
        return alamat;
    }
}
